async function renderBlog(language) {
    const url_params = new URLSearchParams(window.location.search);
    const blog_id = url_params.get("id");
    console.log(blog_id);
    const blogs = blogDictionary;

    if (blog_id) {
        try {
            console.log(blogs);
            const blog = blogs[language][blog_id];
            if (blog) {
                const title = document.getElementById("post__title");
                const blog_desc = document.getElementById("post__desc");
                const blog_image = document.getElementById("post__img");

                title.innerHTML = blog.title;
                blog_desc.innerHTML = blog.paragraph;
                blog_image.src = blog.img;


            }
            else {
                console.error("Project not found");
            }
        } catch (error) {
            console.error("Error blogs", error);
        }
    }
}

function renderBlogDetails() {
    var language = localStorage.getItem("language");
    renderBlog(language);
    document.dir = language === 'ar' ? 'rtl' : 'ltr';
}


$(document).on('languageChanged', function () {
    console.log('Language changed');
    renderBlogDetails();
});

